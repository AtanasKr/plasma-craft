let _gallery = [];
let _galleryIdx = 0;

function openProductModal(card) {
    const lang = localStorage.getItem('lang') || 'bg';
    const titleKey = card.getAttribute('data-key-title');
    const descKey  = card.getAttribute('data-key-desc');
    const title = (titleKey && translations[lang]?.[titleKey])
        || card.querySelector('.product-card-name')?.textContent || '';
    const desc  = (descKey  && translations[lang]?.[descKey])  || '';
    const raw   = card.getAttribute('data-images') || '';
    _gallery    = raw.split(',').map(s => s.trim()).filter(Boolean);
    _galleryIdx = 0;

    const modal = document.getElementById('productModal');
    if (!modal) return;

    document.getElementById('modalProductTitle').textContent = title;
    document.getElementById('modalProductDesc').textContent  = desc;
    _renderModal();
    modal.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    const modal = document.getElementById('productModal');
    if (modal) modal.classList.remove('open');
    document.body.style.overflow = '';
}

function galleryPrev() {
    if (!_gallery.length) return;
    _galleryIdx = (_galleryIdx - 1 + _gallery.length) % _gallery.length;
    _renderModal();
}

function galleryNext() {
    if (!_gallery.length) return;
    _galleryIdx = (_galleryIdx + 1) % _gallery.length;
    _renderModal();
}

function setGalleryIdx(i) {
    _galleryIdx = i;
    _renderModal();
}

function _renderModal() {
    const mainImg    = document.getElementById('modalMainImg');
    const placeholder = document.getElementById('modalImgPlaceholder');
    const thumbsEl   = document.getElementById('modalThumbsStrip');
    const prevBtn    = document.getElementById('modalPrev');
    const nextBtn    = document.getElementById('modalNext');

    const src = _gallery[_galleryIdx] || '';
    if (mainImg) {
        if (src) {
            mainImg.src = src;
            mainImg.style.display = 'block';
            if (placeholder) placeholder.style.display = 'none';
        } else {
            mainImg.style.display = 'none';
            if (placeholder) placeholder.style.display = 'flex';
        }
    }

    const showNav = _gallery.length > 1;
    if (prevBtn) prevBtn.style.display = showNav ? 'flex' : 'none';
    if (nextBtn) nextBtn.style.display = showNav ? 'flex' : 'none';

    if (thumbsEl) {
        thumbsEl.innerHTML = _gallery.map((s, i) => {
            const cls = i === _galleryIdx ? 'modal-thumb active' : 'modal-thumb';
            return s
                ? `<div class="${cls}" onclick="setGalleryIdx(${i})"><img src="${s}" alt=""></div>`
                : `<div class="${cls}" onclick="setGalleryIdx(${i})">${i + 1}</div>`;
        }).join('');
    }
}

document.addEventListener('keydown', e => {
    const modal = document.getElementById('productModal');
    if (!modal?.classList.contains('open')) return;
    if (e.key === 'Escape') closeModal();
    if (e.key === 'ArrowLeft')  galleryPrev();
    if (e.key === 'ArrowRight') galleryNext();
});
