document.addEventListener('DOMContentLoaded', () => {
    // Close mobile menu on link click
    document.querySelectorAll('.mobile-menu .nav-pill').forEach(link => {
        link.addEventListener('click', () => {
            document.getElementById('mobileMenu')?.classList.remove('open');
            document.getElementById('hamburgerBtn')?.classList.remove('open');
        });
    });

    // Sidebar scroll spy — works on all pages with anchor sidebar links
    const sideLinks = document.querySelectorAll('.sidebar-link[href^="#"]');
    if (sideLinks.length) {
        const targets = [...sideLinks]
            .map(l => document.querySelector(l.getAttribute('href')))
            .filter(Boolean);
        if (targets.length) {
            // Activate first item by default
            sideLinks[0].classList.add('active');
            const observer = new IntersectionObserver(entries => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        sideLinks.forEach(l => l.classList.remove('active'));
                        const active = document.querySelector(`.sidebar-link[href="#${entry.target.id}"]`);
                        if (active) active.classList.add('active');
                    }
                });
            }, { rootMargin: '-20% 0px -70% 0px' });
            targets.forEach(t => observer.observe(t));
        }
    }

    // Nav dropdown: click to toggle on touch devices
    document.querySelectorAll('.nav-dropdown .dropdown-trigger').forEach(btn => {
        btn.addEventListener('click', e => {
            const parent = btn.closest('.nav-dropdown');
            const isOpen = parent.classList.contains('open');
            document.querySelectorAll('.nav-dropdown').forEach(d => d.classList.remove('open'));
            if (!isOpen) parent.classList.add('open');
            e.stopPropagation();
        });
    });

    document.addEventListener('click', () => {
        document.querySelectorAll('.nav-dropdown').forEach(d => d.classList.remove('open'));
    });
});

